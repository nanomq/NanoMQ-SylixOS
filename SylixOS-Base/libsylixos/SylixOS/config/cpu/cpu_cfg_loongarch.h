#ifndef __CPU_CFG_LOONGARCH_H
#define __CPU_CFG_LOONGARCH_H

/*********************************************************************************************************
  CPU 体系结构
*********************************************************************************************************/

#define LW_CFG_CPU_ARCH_LOONGARCH       1                               /*  CPU 架构                    */
#define LW_CFG_CPU_ARCH_FAMILY          "LoongArch(R)"                  /*  LoongArch family            */

/*********************************************************************************************************
  SMT 同步多线程调度优化
*********************************************************************************************************/

#define LW_CFG_CPU_ARCH_SMT             0                               /*  同步多线程优化              */

/*********************************************************************************************************
  CACHE LINE 对齐
*********************************************************************************************************/

#define LW_CFG_CPU_ARCH_CACHE_LINE      64                              /*  cache 最大行对齐属性        */

/*********************************************************************************************************
  CPU 字长与整型大小端定义
*********************************************************************************************************/

#if defined(__GNUC__)
#if __BYTE_ORDER__ == __ORDER_LITTLE_ENDIAN__
#define LW_CFG_CPU_ENDIAN               0                               /*  0: 小端                     */
#else 
#define LW_CFG_CPU_ENDIAN               1                               /*  1: 大端                     */
#endif                                                                  /*  __BYTE_ORDER__ == LITTLE    */
#else
#define LW_CFG_CPU_ENDIAN               0                               /*  0: 小端  1: 大端            */
#endif                                                                  /*  defined(__GNUC__)           */

#if _LOONGARCH_SZLONG == 32
#define LW_CFG_CPU_WORD_LENGHT          32                              /*  CPU 字长                    */
#define LW_CFG_CPU_PHYS_ADDR_64BIT      0                               /*  物理地址 32bit 宽度         */
#else
#define LW_CFG_CPU_WORD_LENGHT          64                              /*  CPU 字长                    */
#define LW_CFG_CPU_PHYS_ADDR_64BIT      1                               /*  物理地址 64bit 宽度         */
#endif

/*********************************************************************************************************
  LoongArch 指令配置
*********************************************************************************************************/

#define LW_CFG_CPU_HAS_UNALIGNED_INST   0

/*********************************************************************************************************
  LoongArch MMU 配置

  LW_CFG_LOONGARCH_PAGE_SHIFT 可配置的值有如下:
        12 :  4K  Bytes per page.
        14 :  16K Bytes per page.
        16 :  64K Bytes per page.
  考虑到 cache alias 问题，并根据原厂建议，默认页面大小设置为 16K
*********************************************************************************************************/

#define LW_CFG_LOONGARCH_PAGE_SHIFT     14                              /*  MMU 页面大小                */

/*********************************************************************************************************
  快速获取线程上下文 (使用 $r21 Platform 特性)
*********************************************************************************************************/

#define LW_CFG_CPU_FAST_TLS             0                               /*  快速获取任务上下文          */

/*********************************************************************************************************
  浮点运算单元
*********************************************************************************************************/

#define LW_CFG_CPU_FPU_EN               1                               /*  CPU 是否拥有 FPU            */

/*********************************************************************************************************
  DSP 数字信号处理器
*********************************************************************************************************/

#define LW_CFG_CPU_DSP_EN               0                               /*  CPU 是否拥有 DSP            */

/*********************************************************************************************************
  ATOMIC
*********************************************************************************************************/

#define LW_CFG_CPU_ATOMIC_EN            1

#if LW_CFG_CPU_WORD_LENGHT == 64
#define LW_CFG_CPU_ATOMIC64_EN          1
#else
#define LW_CFG_CPU_ATOMIC64_EN          0
#endif

#endif                                                                  /*  __CPU_CFG_LOONGARCH_H       */
/*********************************************************************************************************
  END
*********************************************************************************************************/
